    #### 内联表单组

    表单组支持内联方式，这种方式一行支持多个表单。表单的宽度会根据一行中表单的数量适配。

    添加`.inline`样式类来使用内联的表单样式。
    
    :::democode/html
    <div style="width:600px;margin:auto">
        <ui-formgroup class="inline">
            <!-- .row : 单行 -->
            <div class="row">
                <!-- .item : 表单项 -->
                <div class="item">
                    <!-- .title : 表单项名称 -->
                    <h5 class="title">Name</h5>
                    <!-- .content : 表单项内容 -->
                    <div class="content">
                        <!-- .content : 表单容器 -->
                        <div class="form">
                            <ui-textinput></ui-textinput>
                        </div>
                    </div>
                </div>

                <div class="item">
                    <h5 class="title">Introduction</h5>
                    <div class="content">
                        <div class="form">
                            <ui-textinput></ui-textinput>
                        </div>
                    </div>
                </div>
            </div>
        </ui-formgroup>
    </div>
    :::
    
